function onSay(player, words, param)
	for taskName, config in pairs(config) do
		if player:getStorageValue(config.storage) > 0 then
			player:sendTextMessage(MESSAGE_INFO_DESCR, "[Task - " .. config.plural .. "] You have killed " .. player:getStorageValue(config.storage) - 1 .. "/" .. config.count .. ".")
			return false
		end
	end
	player:sendTextMessage(MESSAGE_INFO_DESCR, "You are not assigned to a task. Pay a visit to Grizzly Adams.")
	return false
end
